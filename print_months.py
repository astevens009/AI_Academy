# Exercise: Write a program that uses a for loop to print out:
#   "One of the months of the year is <input month here>"
# for each month in a year

print()

for month in ["January",
              "February",
              "March",
              "April",
              "May",
              "June",
              "July",
              "August",
              "September",
              "October",
              "November",
              "December"]:
    print("One of the months of the year is {}".format(month))

print()
